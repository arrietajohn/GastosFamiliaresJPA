    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try {
            var request = new FindUserByIdRequestDto(
                    mainWindow.getLoginResponse().getUserCode());
            var userDao = new UserDao();
            var useCase = new FindUserByIdUseCase(userDao);
            var controlador = new FindUserByIdController(useCase);
            var response = controlador.execute(request);
            mostarDatos(response);
        } catch (Exception error) {
            JOptionPane.showMessageDialog(this, error.getMessage(), "Verificar",
                    JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_formWindowOpened

