    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        var code = codeField.getText();
        var password1 = String.valueOf(password1Field.getPassword());
        var password2 = String.valueOf(password2Field.getPassword());
        var name = nameField.getText();
        var lastName = lastNameField.getText();
        var role = UserRoleEnum.USER;
        var email = emailField.getText();
        if (password1.trim().isEmpty() || password1.trim().isEmpty()) {
            var mensaje = "El password es requerido";
            JOptionPane.showMessageDialog(this, mensaje, "Verificar",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!password1.equals(password2)) {
            var mensaje = "Password1 deber ser igual a Password2";
            JOptionPane.showMessageDialog(this, mensaje, "Verificar",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            var request = new EditUserRequestDto(
                    code,
                    password1,
                    name,
                    lastName,
                    role,
                    email);

            var userDao = new UserDao();
            var useCase = new EditUserUseCase(userDao);
            var controller = new EditUserController(useCase);
            var response = controller.execute(request);
            mainWindow.setLoginResponse(transformarALoginRespuestaDto(response));
            JOptionPane.showMessageDialog(this, "Usuario actualizado con exito");
        } catch (Exception error) {
            JOptionPane.showMessageDialog(this, error.getMessage(), "Verificar",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_updateButtonActionPerformed

