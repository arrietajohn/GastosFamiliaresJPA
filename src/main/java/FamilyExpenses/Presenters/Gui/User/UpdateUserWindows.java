    private void mostarDatos(FindUserByIdResponseDto userResponse) {

        lastNameField.setText(userResponse.getLastName());
        codeField.setText(userResponse.getUserCode());
        emailField.setText(userResponse.getEmail());
        nameField.setText(userResponse.getFirstName());
        password1Field.setText(userResponse.getPassword());
        password2Field.setText(userResponse.getPassword());
        switch (userResponse.getRole()) {
            case ADMINISTRATOR ->
                administratorRoleRadioButton.setSelected(true);
            case USER ->
                userRoleRadioButton.setSelected(true);
            case MEMBER ->
                memberRoleRadioButton.setSelected(true);
            default -> {
            }
        }
    }

    private LoginResponseDto transformarALoginRespuestaDto(EditUserResponseDto response) {
        return new LoginResponseDto(
                response.getUserCode(),
                response.getFirstName(),
                response.getLastName(),
                response.getRole(),
                response.getEmail());
    }


